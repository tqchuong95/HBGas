  List<ProductDetails> getListProducts(String listValue) {
    List<ProductDetails> tmp;
    switch (listValue.toLowerCase()) {
      case 'bottle':
        tmp = bottle;
        break;
      case 'stove':
        tmp = stove;
        break;
      case 'accessories':
        tmp = accessories;
        break;
      default:
        tmp = allList;
        break;
    }
    return tmp;
  }

  ProductDetails getProduct(String productId) {
    for (int i = 0; i < all.length; i++) {
      if (all[i]['id'] == productId) {
        return ProductDetails(
          productID: all[i]['id'],
